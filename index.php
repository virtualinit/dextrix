<h2 class="title">Welcome to dextrix Information services!</h2>
<div class="story">
<p><strong>Dextrix</strong> is a junction of symposium, technical festival and open source meet. The first of its kind in NCR India. Dextrix 2009 is going to be unique platform for technocrats, open source lovers, pioneers, faculties and students. Asia Pacific Institute of Information Technology (Panipat) proudly announces the  assemble on 30th and 31st of October 2009 at APIIT Panipat Campus.</p>
