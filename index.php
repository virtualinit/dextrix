<div class="post">
<h2 class="title">technical symposium and festival </h2>
<div class="story">
<p>Innovations is the event holding promises for paper/idea presenters. Virtualization Workshop has been turned into <a href="http://fedoraproject.org/wiki/events" class="style3">Fedora Event</a>. 500 companies are being approached for sponsorship &amp; 2000 national and international engineering colleges are being invited. Dextrix Night will accommodate FARIDKOT Performance on 30th night and Reboot 2009 on 31st October.</p>
<blockquote>
<p>Technocrats who wish to present their paper, review, idea, project, add-on, supplement, technology and/or others must drop their final abstract [*.pdf] at <a href="mailto:innovations@dextrix.org" class="style3">innovations@dextrix.org</a>. Events Registrations are open at <a href="http://dextrix.org" class="style3">http://dextrix.org</a></p>
