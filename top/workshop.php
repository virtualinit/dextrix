<h2 class="title">virtualization</h2>
<div class="story">
<p> The workshop begins with a comprehensive overview of the virtualization landscape. Attendees will learn about important drivers, key benefits, potential challenges, best practices, and significant market trends across a full range of virtualization platforms and technologies. Dextrix Virtualization Workshop Team will  lead a group discussion to assess your environment, investigate specific issues in your data center, and uncover potential impacts. Team will practically show the implementation of KVM-Virtualization on Fedora 11.  Team will also provide an overview of available virtualization solutions and review key decision factors when selecting a virtualization vendor.</p>
